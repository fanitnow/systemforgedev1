  id                  UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at          TIMESTAMPTZ DEFAULT now(),
  client_id           UUID REFERENCES paid_clients(id),
  score               INTEGER CHECK (score BETWEEN 1 AND 5),
  testimonial         TEXT,
  permission_to_share BOOLEAN DEFAULT false,
  survey_source       TEXT DEFAULT 'builder_chat',
  follow_up_needed    BOOLEAN DEFAULT false
);


-- ═══ BLOCK 4: GUARD AGENT TABLES ══════════════════════════════

-- Every support issue raised through the Guard agent
CREATE TABLE support_tickets (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  updated_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT REFERENCES chat_sessions(session_id),
  client_id       UUID REFERENCES paid_clients(id),  -- nullable — non-clients can raise issues
  lead_id         UUID REFERENCES leads(id),           -- if they haven't purchased
  issue_type      TEXT NOT NULL,               -- refund / late_delivery / billing / quality / other
  severity        TEXT DEFAULT 'medium',       -- low / medium / high / critical
  description     TEXT,                        -- client's exact words from chat
  conversation    JSONB DEFAULT '[]',          -- full guard conversation
  status          TEXT DEFAULT 'open',         -- open / in_review / resolved / escalated / closed
  resolution      TEXT,                        -- refund / redo / resolved / escalated / dismissed
  resolution_notes TEXT,
  resolved_at     TIMESTAMPTZ,
  owner_notified  BOOLEAN DEFAULT false,
  owner_notified_at TIMESTAMPTZ,
  refund_id       UUID                         -- FK added after refund_log created
);

-- Financial record of every refund processed
CREATE TABLE refund_log (
  id                  UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at          TIMESTAMPTZ DEFAULT now(),
  client_id           UUID REFERENCES paid_clients(id),
  ticket_id           UUID REFERENCES support_tickets(id),
  stripe_refund_id    TEXT,                    -- from Stripe API
  original_charge_id  TEXT,                    -- original Stripe payment intent ID
  amount_cents        INTEGER NOT NULL,         -- amount refunded in cents
  reason              TEXT,                    -- late_delivery / not_as_described / client_request
  refund_type         TEXT DEFAULT 'full',     -- full / partial
  processed_at        TIMESTAMPTZ,
  processed_by        TEXT DEFAULT 'owner'
);


