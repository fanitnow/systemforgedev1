-- ═══ BLOCK 2: FORGE + SCOUT AGENT TABLES ══════════════════════

-- Full conversation log for every Forge (sales qualifier) session
CREATE TABLE forge_conversations (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT REFERENCES chat_sessions(session_id),
  lead_id         UUID REFERENCES leads(id),
  messages        JSONB DEFAULT '[]',          -- [{role, content, timestamp}]
  business_type   TEXT,                        -- extracted from conversation
  visitor_intent  TEXT,                        -- audit / pricing / support / info / browse
  routed_to       TEXT,                        -- which agent they were sent to
  routed_at       TIMESTAMPTZ,
  source_url      TEXT,
  referrer        TEXT
);

-- One record per audit session initiated by Scout
CREATE TABLE audit_sessions (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT REFERENCES chat_sessions(session_id),
  lead_id         UUID REFERENCES leads(id),
  started_at      TIMESTAMPTZ DEFAULT now(),
  completed_at    TIMESTAMPTZ,
  questions_asked INTEGER DEFAULT 0,
  source_agent    TEXT DEFAULT 'forge'         -- which agent handed off to Scout
);

-- Full audit output — generated by Scout after Q&A sequence
CREATE TABLE audit_reports (
  id                  UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at          TIMESTAMPTZ DEFAULT now(),
  session_id          TEXT REFERENCES chat_sessions(session_id),
  lead_id             UUID REFERENCES leads(id),
  audit_session_id    UUID REFERENCES audit_sessions(id),
  business_type       TEXT,
  business_name       TEXT,
  q_and_a             JSONB DEFAULT '[]',      -- [{question, answer}] all 7 pairs
  full_conversation   JSONB DEFAULT '[]',      -- complete scout conversation
  identified_gaps     TEXT[],                  -- array of gap descriptions
  revenue_leak_low    INTEGER,                 -- monthly low estimate in dollars
  revenue_leak_high   INTEGER,                 -- monthly high estimate in dollars
  recommended_tier    INTEGER CHECK (recommended_tier IN (1,2,3)),
  report_shown        BOOLEAN DEFAULT false,
  cta_clicked         BOOLEAN DEFAULT false,
  converted           BOOLEAN DEFAULT false,
  converted_at        TIMESTAMPTZ,
  converted_tier      INTEGER
);


-- ═══ BLOCK 3: BUILDER AGENT TABLES ════════════════════════════

-- Chat conversations between Builder agent and paying clients
CREATE TABLE builder_conversations (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT REFERENCES chat_sessions(session_id),
