-- Auto-update updated_at on leads and support_tickets
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = now(); RETURN NEW; END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER leads_updated_at
  BEFORE UPDATE ON leads
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

CREATE TRIGGER tickets_updated_at
  BEFORE UPDATE ON support_tickets
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- Auto-flag leads for Closer when audit completes without purchase
CREATE OR REPLACE FUNCTION flag_unconverted_audit()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.report_shown = true AND NEW.converted = false THEN
    INSERT INTO closer_leads (lead_id, audit_report_id, flag_reason)
    VALUES (NEW.lead_id, NEW.id, 'audit_no_purchase')
    ON CONFLICT DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER auto_flag_closer_lead
  AFTER UPDATE ON audit_reports
  FOR EACH ROW
  WHEN (OLD.report_shown IS DISTINCT FROM NEW.report_shown)
  EXECUTE FUNCTION flag_unconverted_audit();

