-- ═══ BLOCK 1: SHARED FOUNDATION TABLES ═══════════════════════

-- Master lead record — created the moment anyone interacts with any agent
CREATE TABLE leads (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  updated_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT,
  name            TEXT,
  email           TEXT,
  phone           TEXT,
  business_type   TEXT,
  business_name   TEXT,
  lead_source     TEXT DEFAULT 'chat',        -- dm / walk-in / organic / referral / ad / chat
  intent_score    INTEGER DEFAULT 5 CHECK (intent_score BETWEEN 1 AND 10),
  status          TEXT DEFAULT 'new',          -- new / warm / hot / converted / lost
  last_agent      TEXT DEFAULT 'forge',        -- forge / scout / builder / guard / closer
  converted       BOOLEAN DEFAULT false,
  converted_at    TIMESTAMPTZ,
  notes           TEXT
);

-- One record per chat window opened on any page
CREATE TABLE chat_sessions (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT UNIQUE NOT NULL,
  lead_id         UUID REFERENCES leads(id),
  current_agent   TEXT DEFAULT 'forge',
  agents_visited  TEXT[] DEFAULT '{forge}',
  source_url      TEXT,
  referrer        TEXT,
  device_type     TEXT,                        -- mobile / desktop / tablet
  total_messages  INTEGER DEFAULT 0,
  duration_seconds INTEGER,
  ended_at        TIMESTAMPTZ
);

-- Every time an agent routes a visitor to another agent
CREATE TABLE agent_handoffs (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT REFERENCES chat_sessions(session_id),
  lead_id         UUID REFERENCES leads(id),
  from_agent      TEXT NOT NULL,               -- forge / scout / builder / guard / closer
  to_agent        TEXT NOT NULL,
  reason          TEXT,                        -- why the handoff was triggered
  trigger_message TEXT                         -- the message that caused the route
);

-- Lightweight event log for page views, CTA clicks, audit starts
CREATE TABLE page_events (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at      TIMESTAMPTZ DEFAULT now(),
  session_id      TEXT,
  event_type      TEXT NOT NULL,               -- page_view / cta_click / audit_start / report_shown / chat_open
  page            TEXT,
  detail          TEXT,
  lead_id         UUID REFERENCES leads(id)
);


